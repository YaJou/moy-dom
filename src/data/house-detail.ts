function buildTechnicalTable(house: House): TechnicalSpecRow[] {
  const { specs } = house;
  return [
    { label: "Фундамент", value: "Монолитная лента" },
    { label: "Материал стен", value: specs.wallMaterial },
    { label: "Облицовка", value: "Облицовочный кирпич" },
    { label: "Кровля", value: "Металлочерепица" },
    { label: "Окна", value: "Пластиковые, в стоимости" },
    { label: "Высота потолков", value: "2,7 м" },
    { label: "Толщина утепления", value: "150–200 мм" },
    { label: "Отопление", value: "Газовое + тёплый пол" },
    { label: "Газ", value: specs.gas },
    { label: "Вода", value: specs.water },
    { label: "Канализация", value: specs.sewage },
    { label: "Электроснабжение", value: specs.electricity === "Нет" ? "Подводится к участку" : specs.electricity },
    { label: "Парковка", value: specs.parking },
    { label: "Забор", value: "По периметру участка" },
    { label: "Дорога", value: specs.road },
  ];
}

